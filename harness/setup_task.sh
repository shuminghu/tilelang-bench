#!/usr/bin/env bash
# Materialize a disposable workspace for one perf-track task.
#
#   setup_task.sh <task_dir> [run_id]
#
# <task_dir>  path to a task definition (must contain task.json, problem.py, solution.py)
# Prints the absolute path of the created workspace on stdout (last line).

source "$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)/common.sh"

TASK_DIR="${1:?usage: setup_task.sh <task_dir> [run_id]}"
RUN_ID="${2:-$(date +%Y%m%d_%H%M%S)_$$}"
[ -d "$TASK_DIR" ] || die "task dir not found: $TASK_DIR"
[ -f "$TASK_DIR/task.json" ] || die "missing task.json in $TASK_DIR"
[ -d "$GOLDEN_VENV" ] || die "golden venv not found: $GOLDEN_VENV"

TASK_ID="$(basename "$TASK_DIR")"
WORK="$RUNS_DIR/$TASK_ID/$RUN_ID"
mkdir -p "$WORK"/{home,tmp,cache,tl_cache,triton,inductor,pyc}

# Agent-visible payload ONLY: the file to edit + the prompt. The grader
# (problem.py, which contains the target kernel + reference) and task.json are
# deliberately NOT placed in the workspace, so an agent cannot read the answer
# out of its own cwd. Scoring loads the grader from the private TASK_DIR.
/bin/cp -f "$TASK_DIR/solution.py" "$WORK/solution.py"
python3 -c "import json,sys; sys.stdout.write(json.load(open('$TASK_DIR/task.json'))['prompt']+'\n')" > "$WORK/prompt.txt"

# Write a per-task env file that (1) activates the read-only golden venv and
# (2) redirects every mutable cache into the disposable workspace so reset is rm -rf.
cat > "$WORK/env.sh" <<EOF
# Auto-generated by setup_task.sh -- source to enter the task environment.
source "$GOLDEN_VENV/bin/activate"
export CUDA_VISIBLE_DEVICES="$CUDA_VISIBLE_DEVICES"
export HOME="$WORK/home"
export TMPDIR="$WORK/tmp"
export XDG_CACHE_HOME="$WORK/cache"
export TILELANG_CACHE_DIR="$WORK/tl_cache"
export TRITON_CACHE_DIR="$WORK/triton"
export TORCHINDUCTOR_CACHE_DIR="$WORK/inductor"
export PYTHONPYCACHEPREFIX="$WORK/pyc"
export CCACHE_DIR="$CCACHE_DIR"
cd "$WORK"
EOF

log "task=$TASK_ID workspace=$WORK"
echo "$WORK"
